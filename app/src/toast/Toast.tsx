import { ReactNode, useCallback, useEffect, useState } from 'react';
import { X } from '../../../lib/src/symbols';

export type ToastType = 'info' | 'warning' | 'error';

export interface ToastAction {
  label: string;
  onClick: () => void;
}

export interface ToastData {
  id: string;
  title: string;
  description?: string;
  type?: ToastType;
  action?: ToastAction;
  duration?: number;
  onRemove?: (id: string) => void;
}

interface ToastProps extends ToastData {
  customTypes?: Record<string, { className: string; icon?: ReactNode }>;
  customComponent?: React.ComponentType<ToastData>;
}

const defaultTypeStyles: Record<ToastType, { className: string; icon: ReactNode }> = {
  info: {
    className: 'bg-blue-50 border-blue-200 text-blue-900 dark:bg-blue-900/20 dark:border-blue-800 dark:text-blue-100',
    icon: (
      <svg className='w-5 h-5' viewBox='0 0 20 20' fill='currentColor'>
        <path
          fillRule='evenodd'
          d='M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z'
          clipRule='evenodd'
        />
      </svg>
    ),
  },
  warning: {
    className:
      'bg-yellow-50 border-yellow-200 text-yellow-900 dark:bg-yellow-900/20 dark:border-yellow-800 dark:text-yellow-100',
    icon: (
      <svg className='w-5 h-5' viewBox='0 0 20 20' fill='currentColor'>
        <path
          fillRule='evenodd'
          d='M8.257 3.099c.765-1.36 2.722-1.36 3.486 0l5.58 9.92c.75 1.334-.213 2.98-1.742 2.98H4.42c-1.53 0-2.493-1.646-1.743-2.98l5.58-9.92zM11 13a1 1 0 11-2 0 1 1 0 012 0zm-1-8a1 1 0 00-1 1v3a1 1 0 002 0V6a1 1 0 00-1-1z'
          clipRule='evenodd'
        />
      </svg>
    ),
  },
  error: {
    className: 'bg-red-50 border-red-200 text-red-900 dark:bg-red-900/20 dark:border-red-800 dark:text-red-100',
    icon: (
      <svg className='w-5 h-5' viewBox='0 0 20 20' fill='currentColor'>
        <path
          fillRule='evenodd'
          d='M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z'
          clipRule='evenodd'
        />
      </svg>
    ),
  },
};

export function Toast({
  id,
  title,
  description,
  type = 'info',
  action,
  duration = 5000,
  onRemove,
  customTypes,
  customComponent: CustomComponent,
}: ToastProps) {
  const [isExiting, setIsExiting] = useState(false);

  const handleRemove = useCallback(() => {
    setIsExiting(true);
    setTimeout(() => {
      onRemove?.(id);
    }, 150); // Match animation duration
  }, [id, onRemove]);

  useEffect(() => {
    if (duration > 0) {
      const timer = setTimeout(() => {
        handleRemove();
      }, duration);

      return () => clearTimeout(timer);
    }
  }, [duration, id, onRemove, handleRemove]);

  if (CustomComponent) {
    return (
      <CustomComponent
        id={id}
        title={title}
        description={description}
        type={type}
        action={action}
        duration={duration}
        onRemove={onRemove}
      />
    );
  }

  // Check custom types first, then fall back to default
  const typeStyle = customTypes?.[type] || defaultTypeStyles[type as ToastType] || defaultTypeStyles.info;

  return (
    <div
      className={`
        relative flex items-start p-4 rounded-lg border shadow-lg transition-all duration-150 ease-in-out
        ${typeStyle.className}
        ${isExiting ? 'opacity-0 translate-x-full' : 'opacity-100 translate-x-0'}
      `}
    >
      {/* Icon */}
      <div className='flex-shrink-0 mr-3'>{typeStyle.icon}</div>

      {/* Content */}
      <div className='flex-grow min-w-0'>
        <div className='font-medium text-sm leading-5'>{title}</div>
        {description && <div className='mt-1 text-sm opacity-90 leading-5'>{description}</div>}
        {action && (
          <div className='mt-3'>
            <button
              onClick={action.onClick}
              className='text-sm font-medium underline hover:no-underline focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-current rounded'
            >
              {action.label}
            </button>
          </div>
        )}
      </div>

      {/* Close button */}
      <button
        onClick={handleRemove}
        className='flex-shrink-0 ml-3 p-1 rounded-md hover:bg-black/10 dark:hover:bg-white/10 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-current'
      >
        <X size={16} />
      </button>
    </div>
  );
}
